<p><strong>Fichier des décès de l'INSEE</strong></p>
    <p>
        Ce service vise à permettre la recherche directe de personnes décédées. Il repose sur la base opendata
        délivrée par l'<a href="https://www.insee.fr/fr/information/4190491" target="_blank">INSEE</a> et
        diffusée par <a href="https://www.data.gouv.fr/fr/datasets/fichier-des-personnes-decedees/" target="_blank">data.gouv.fr</a>.
    </p>
<p><strong>Exhaustivité des données</strong></p>
    <p>
        La recherche utilise l'intégralité des données INSEE, soit environ 25 millions d'enregistrements.
        Elle concerne les déclarations de décès retransmises vers l'INSEE depuis 1970 jusqu'au mois précédent le mois
        en cours. Les données du mois précédent sont indexées vers le 10 du mois, selon la disponibilité des données INSEE.
    <p>
        Seules les personnes ayant eu un numéro INSEE et ayant fait l'objet d'une déclaration de décès, en mairie ou
        en consulat sont enregistrées (cf. plus bas, références sur le processus administratif).
    </p>
    <p>
        Des erreurs liées à l'indexation peuvent subsister, nous nous tenons à disposition
        en cas de problème d'exhaustivité (<a href="mailto:matchid.project@gmail.com">matchid.project@gmail.com</a>)
        pour confirmer l'absence du fichier d'origine.
    </p>
<p><strong>Je ne trouve pas une personne décédée</strong></p>
    <p>
        Les données INSEE sont issues d'un processus de collecte administratif auprès
        des collectivités. Des absence et des erreurs sont possibles, voici les
        cas que nous avons identifié :
    </p>
    <ul>
        <li>
            Le décès est récent - le délai de transmission des mairies ou du consulat à l'INSEE peut varier,
            il n'existe pas de "garantie" quant au délai.
        </li>
        <li>
            Avant 1990, le process INSEE était peu informatisé. Le processus de tranmission
            par formulaire papier semble avoir généré des problèmes d'exhaustivité.
        </li>
        <li>
            Seuls les personnes ayant eu un numéro INSEE sont inscrites au fichier. Un
            parcours de vie (e.g. naissance / décès à l'étranger)
            peut expliquer l'absence du fichier.
        </li>
    </ul>
    <p>
        Si vous avez l'assurance que la personne concernée disposait d'un numéro INSEE, il est important
        de savoir que celle-ci figure peut être toujours au fichier RNIPP. Il est peut être
        alors nécessaire de contacter la mairie ou le consultat de décès pour vous assurer
        de la bonne transmission à l'INSEE.
    </p>
    <p>
        Si tel est le cas, n'hésitez pas à nous écrire à <a href="mailto:matchid.project@gmail.com">matchid.project@gmail.com</a>
        pour que nous confirmions l'absence du fichier.
    </p>
    <p>
        Nous ne pourrons vous accompagner plus loin dans la démarche administrative à ce stade.
    </p>
<p><strong>Une erreur figure dans l'enregistrement du décès</strong></p>
    <p>
        Nous ne sommes pas habilités à modifier les données de l'INSEE.
    </p>
    <p>
        En cas d'erreur (e.g enregistrement du nom d'époux au lieu du nom de naissance), seuls
        la mairie et le consultat de décès sont habilités à transmettre
        un correctif à l'INSEE. Nous vous invitons donc à prendre contact avec le service
        d'État civil de la mairie ou du consulat du décès.
    </p>
